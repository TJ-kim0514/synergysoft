CREATE TABLE REPORT (
	REPORT_ID	        VARCHAR2(255),
	POST_ID	            VARCHAR2(255) NOT NULL,
	REPORT_USER_ID	    VARCHAR2(255) NOT NULL,
	REPORTING_REASON	VARCHAR2(255),
